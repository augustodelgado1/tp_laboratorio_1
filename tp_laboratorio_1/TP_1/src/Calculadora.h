

#include <math.h>

/// @fn int CalcularDivision(int, int, float*)
/// @brief Permite Realizar una division y devuelve el resultado
///
/// @param int el primer numero divisor
/// @param int dividendo
/// @param float* donde se va a guardar el resultado
/// @return [1] si se pudo realizar el calculo, [0] si hubo error
int CalcularDivision(int,int,float*);

/// @fn int CalcularSuma(int, int)
/// @brief Permite Realizar una suma y devuelve el resultado
///
/// @param int el primer numero
/// @param int el segundo numero
/// @return el resultado
int CalcularSuma(int,int);

/// @fn int CalcularResta(int, int)
/// @brief Permite Realizar una Resta y devuelve el resultado
///
/// @param int el primer numero
/// @param int el segundo numero
/// @return el resultado
int CalcularResta(int,int);

/// @fn int CalcularMultiplicacion(int, float)
/// @brief Permite Realizar una Multiplicacion y devuelve el resultado
///
/// @param int el primer numero
/// @param float el segundo numero divisor
/// @return el resultado
int CalcularMultiplicacion(int,float);
